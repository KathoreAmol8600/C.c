 #include<stdio.h>
void main()
{
    for(int i=1;i<=10;i++)
    {
        for(int j=1;j<=10;j++)
        {
            // printf(" * ");
            if((j<=6-i&&i<=5)||(j<=i-5&&i>=5)||(j>=5+i&&i<=5)||(j>=16-i&&i>=5))
            {
                printf(" * ");
            }else{
                printf("   ");
            }
        }
        printf("\n");
    }
}